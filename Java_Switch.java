public class Java_Switch {
    public static void main(String args[]){
        int number = 1;
        switch(number) {
            case 3 : System.out.println("Samosa");
                        break;
            case 1 : System.out.println("Burger");
                        break;
            case 2 : System.out.println("mango");
                        break;
            default: ;
        }
    }
}
